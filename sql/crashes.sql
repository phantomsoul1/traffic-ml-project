    Source FLOAT,
    TMC VARCHAR(255),
    Severity INT,
    Start_Time	DATETIME,
    End_Time	DATETIME,
    Start_Lat	FLOAT,
    Start_Lng	FLOAT,
    End_Lat	FLOAT,
